// Get saved language or default to English
let currentLanguage = localStorage.getItem('language') || 'en';

// Function to switch language
function switchLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Update current language display
    const langDisplay = document.getElementById('currentLang');
    if (langDisplay) {
        langDisplay.textContent = lang.toUpperCase();
    }
    
    // Update HTML lang attribute
    document.documentElement.setAttribute('lang', lang);
    
    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            // Handle elements with icons
            const icons = element.querySelectorAll('i');
            if (icons.length > 0) {
                // Store icon HTML
                const iconHTML = Array.from(icons).map(i => i.outerHTML).join(' ');
                // Get text position relative to icon
                const hasIconFirst = element.innerHTML.trim().startsWith('<i');
                
                if (hasIconFirst) {
                    element.innerHTML = iconHTML + ' ' + translations[lang][key];
                } else {
                    element.innerHTML = translations[lang][key] + ' ' + iconHTML;
                }
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Translate placeholder attributes
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('placeholder', translations[lang][key]);
        }
    });
    
    // Update dynamic toggle buttons if they exist
    const toggleProgramsBtn = document.getElementById('toggleProgramsBtn');
    if (toggleProgramsBtn) {
        const hiddenPrograms = document.querySelectorAll('.hidden-program');
        const isVisible = hiddenPrograms.length > 0 && hiddenPrograms[0].style.display !== 'none';
        if (isVisible) {
            const hideText = lang === 'ar' ? 'عرض أقل' : 'Show Less Programs';
            toggleProgramsBtn.innerHTML = '<i class="fas fa-minus me-2"></i>' + hideText;
        } else {
            const showText = lang === 'ar' ? 'عرض كل البرامج' : 'Show All Programs';
            toggleProgramsBtn.innerHTML = '<i class="fas fa-plus me-2"></i>' + showText;
        }
    }
    
    const toggleDiplomasBtn = document.getElementById('toggleDiplomasBtn');
    if (toggleDiplomasBtn) {
        const hiddenDiplomas = document.querySelectorAll('.hidden-diploma');
        const isVisible = hiddenDiplomas.length > 0 && hiddenDiplomas[0].style.display !== 'none';
        if (isVisible) {
            const hideText = lang === 'ar' ? 'عرض أقل' : 'Show Less Diplomas';
            toggleDiplomasBtn.innerHTML = '<i class="fas fa-minus me-2"></i>' + hideText;
        } else {
            const showText = lang === 'ar' ? 'عرض كل الدبلومات' : 'Show All Diplomas';
            toggleDiplomasBtn.innerHTML = '<i class="fas fa-plus me-2"></i>' + showText;
        }
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    switchLanguage(currentLanguage);
});
